const Block = require("./block");

class Piece{
  constructor(){
    this.shapes = {
      "i": [[750,405],[750,435],[750,465],[750,495]],
      "o": [[735,435],[735,465],[765,435],[765,465]],
      "t": [[720,465],[750,435],[750,465],[780,465]],
      "s": [[720,465],[750,465],[750,435],[780,435]],
      "z": [[720,435],[750,435],[750,465],[780,465]],
      "j": [[720,435],[720,465],[750,465],[780,465]],
      "l": [[720,465],[750,465],[780,465],[780,435]]
    };


    this.colors = {
      "i": ["rgb(251, 44, 44)","rgb(176, 118, 118)"],
      "o": ["rgb(247, 220, 89)","rgb(186, 160, 150)"],
      "t": ["rgb(77, 166, 48)","rgb(93, 133, 60)"],
      "s": ["rgb(42, 176, 154)","rgb(75, 143, 132)"],
      "z": ["rgb(31, 82, 192)","rgb(76, 99, 148)"],
      "j": ["rgb(115, 76, 148)","rgb(113, 98, 125)"],
      "l": ["rgb(192, 16, 174)","rgb(129, 79, 124)"]
    };
  }

  addPiece(type){
    const piece = [];
    this.shapes[type].forEach((el)=>{
      piece.push(new Block({x:el[0],y:el[1], width: 30, height: 30, colors: this.colors[type]}));
    });
    return piece;
  }


}

module.exports = Piece;
